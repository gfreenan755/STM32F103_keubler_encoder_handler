/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
 if(enc_cnt > 47){
    if(enc_cnt & 1){
      GPIOB->BSRR = ENC_OUT_Pin << 16;    //RESET
      if (GPIOB->IDR & ENC_IN_Pin){
        if ((enc_cnt <= 97) && (enc_cnt >= 75)){
          enc_turn_tmp |= 1 << ((enc_cnt - 74) >> 1);
        }
        else {
          enc_angle_tmp |= 1 << ((enc_cnt - 48) >> 1);    
        } 
      }
    }
    else{
       GPIOB->BSRR = ENC_OUT_Pin;    //SET
    }
  }
  else if(enc_cnt > 0){
    GPIOB->BSRR = ENC_OUT_Pin;    //SET
  }
  else{
    enc_cnt = 99;
    enc_flag = 1;
  }
  
  if (enc_flag == 0){
  enc_cnt--;
  }
  else {
  HAL_TIM_Base_Stop(&htim2);
  enc_angle = enc_angle_tmp;    
  enc_turn = enc_turn_tmp;
  enc_angle_tmp = 0;
  enc_turn_tmp = 0;
  GPIOC->BSRR = GPIO_PIN_13 << 16;    //RESET
  }

     
  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */

  /* USER CODE END TIM2_IRQn 1 */
}

